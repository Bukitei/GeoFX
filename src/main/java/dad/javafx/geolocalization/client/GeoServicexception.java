package dad.javafx.geolocalization.client;

@SuppressWarnings("serial")
public class GeoServicexception extends Exception {

	public GeoServicexception() {
		
	}

	public GeoServicexception(String message) {
		super(message);
		
	}

	public GeoServicexception(Throwable cause) {
		super(cause);
		
	}

	public GeoServicexception(String message, Throwable cause) {
		super(message, cause);
		
	}

	public GeoServicexception(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
